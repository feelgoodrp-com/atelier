using System.Text;

namespace Feelgood.Atelier.Sidecar.Engine.Build.Targets;

/// <summary>
/// alt:V output (folder-emulated rpf layout, behavior per grzyClothTool):
/// <code>
/// outDir/&lt;resourceName&gt;[_partN]/
///   resource.toml + stream.toml
///   stream/&lt;dlc&gt;_male.rpf/                 (FOLDER named .rpf)
///     mp_m_freemode_01_&lt;dlc&gt;.ymt
///     mp_m_freemode_01_&lt;dlc&gt;/               inner ydd/ytd/yld files
///   stream/&lt;dlc&gt;_male_p.rpf/mp_m_freemode_01_p_&lt;dlc&gt;/   prop files
///   stream/creaturemetadata.rpf/mp_creaturemetadata_&lt;g&gt;_&lt;dlc&gt;.ymt
///   stream/mp_m_freemode_01_&lt;dlc&gt;.meta      (listed in stream.toml [meta])
/// </code>
/// </summary>
public static class AltVBuilder
{
    public static BuildReport Build(BuildPlan plan, string outDir, BuildProgress progress)
    {
        var resources = new List<BuildResourceReport>();
        var warnings = new List<string>(plan.Warnings);

        var totalFiles = plan.Parts.Sum(p => p.Files.Count);
        var copied = 0;

        foreach (var part in plan.Parts)
        {
            var partFolder = Path.Combine(outDir, part.FolderName);
            var streamFolder = Path.Combine(partFolder, "stream");
            Directory.CreateDirectory(streamFolder);

            var metaFiles = new List<string>();

            foreach (var gender in part.Genders)
            {
                var genderSuffix = gender.Gender == "female" ? "_female" : "_male";
                var componentsRpfFolder = Path.Combine(streamFolder, $"{part.DlcName}{genderSuffix}.rpf");
                var componentsInner = Path.Combine(componentsRpfFolder, $"{gender.PedName}_{part.DlcName}");
                Directory.CreateDirectory(componentsInner);

                string? propsInner = null;
                if (gender.Props.Count > 0)
                {
                    propsInner = Path.Combine(
                        streamFolder, $"{part.DlcName}{genderSuffix}_p.rpf", $"{gender.PedName}_p_{part.DlcName}");
                    Directory.CreateDirectory(propsInner);
                }

                progress("ymt", 0, 1, $"Erzeuge CPedVariationInfo für {gender.PedName} ({part.DlcName})");
                File.WriteAllBytes(
                    Path.Combine(componentsRpfFolder, StreamNames.Ymt(gender.PedName, part.DlcName)),
                    YmtGenerator.BuildYmt(gender));

                foreach (var file in part.Files.Where(f => f.Gender == gender.Gender))
                {
                    var targetFolder = file.IsProp ? propsInner : componentsInner;
                    if (targetFolder == null) continue;
                    File.Copy(file.SourcePath, Path.Combine(targetFolder, file.InnerName), overwrite: true);
                    copied++;
                    progress("copy", copied, totalFiles, file.InnerName);
                }

                var creatureBytes = CreatureMetadataGenerator.Build(gender);
                if (creatureBytes != null)
                {
                    var creatureFolder = Path.Combine(streamFolder, "creaturemetadata.rpf");
                    Directory.CreateDirectory(creatureFolder);
                    File.WriteAllBytes(
                        Path.Combine(creatureFolder, StreamNames.CreatureMetadata(gender.GenderLetter, part.DlcName)),
                        creatureBytes);
                }

                if (plan.Options.GenerateShopMeta)
                {
                    var metaName = $"{gender.PedName}_{part.DlcName}.meta";
                    File.WriteAllText(
                        Path.Combine(streamFolder, metaName),
                        ShopMetaGenerator.Build(gender, creatureBytes != null));
                    metaFiles.Add(metaName);
                }
            }

            progress("meta", 0, 1, $"Schreibe TOML-Manifeste für {part.FolderName}");
            File.WriteAllText(Path.Combine(partFolder, "resource.toml"), BuildResourceToml());
            File.WriteAllText(Path.Combine(partFolder, "stream.toml"), BuildStreamToml(metaFiles));
            BuildCommon.WriteBuildManifest(partFolder, "altv", part.DlcName, part.DrawableCount);

            resources.Add(new BuildResourceReport(part.FolderName, part.DrawableCount));
        }

        return new BuildReport(resources, warnings);
    }

    private static string BuildResourceToml()
    {
        var sb = new StringBuilder();
        sb.AppendLine("# generated by atelier by feelgood");
        sb.AppendLine();
        sb.AppendLine("type = 'dlc'");
        sb.AppendLine("main = 'stream.toml'");
        sb.AppendLine("client-files = [ 'stream/*' ]");
        return sb.ToString();
    }

    private static string BuildStreamToml(List<string> metaFiles)
    {
        var sb = new StringBuilder();
        sb.AppendLine("# generated by atelier by feelgood");
        sb.AppendLine();
        sb.AppendLine("files = [");
        sb.AppendLine("'stream/*',");
        sb.AppendLine("]");
        sb.AppendLine();
        sb.AppendLine("[meta]");
        foreach (var metaFile in metaFiles)
            sb.AppendLine($"'stream/{metaFile}' = 'SHOP_PED_APPAREL_META_FILE'");
        return sb.ToString();
    }
}
